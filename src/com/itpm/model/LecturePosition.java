package com.itpm.model;

/**
 *
 * @author Amal
 */
public class LecturePosition {

    private String lecturerPositionLevel;
    private String lecturerPositionName;

    /**
     * @return the lecturerPositionLevel
     */
    public String getLecturerPositionLevel() {
        return lecturerPositionLevel;
    }

    /**
     * @param lecturerPositionLevel the lecturerPositionLevel to set
     */
    public void setLecturerPositionLevel(String lecturerPositionLevel) {
        this.lecturerPositionLevel = lecturerPositionLevel;
    }

    /**
     * @return the lecturerPositionName
     */
    public String getLecturerPositionName() {
        return lecturerPositionName;
    }

    /**
     * @param lecturerPositionName the lecturerPositionName to set
     */
    public void setLecturerPositionName(String lecturerPositionName) {
        this.lecturerPositionName = lecturerPositionName;
    }

}
