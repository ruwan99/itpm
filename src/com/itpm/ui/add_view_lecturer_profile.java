    private void tblLecturersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLecturersMouseClicked
        int selectedRaw = tblLecturers.getSelectedRow();
        if (selectedRaw != -1) {
            DefaultTableModel dtm = (DefaultTableModel) tblLecturers.getModel();
            id = Validations.getIntOrZeroFromString(dtm.getValueAt(selectedRaw, 0).toString());
            txtName.setText(dtm.getValueAt(selectedRaw, 1).toString());
            txtRank.setText(dtm.getValueAt(selectedRaw, 3).toString());
            comboLevel.setSelectedItem(dtm.getValueAt(selectedRaw, 2).toString());
        }
    }//GEN-LAST:event_tblLecturersMouseClicked

