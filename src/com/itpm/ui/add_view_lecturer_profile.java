    private void addLecturer() {
        try {
            Lecturer lec = new Lecturer();
            lec.setLectuerName(txtName.getText().trim());
            lec.setLevel(comboLevel.getSelectedItem().toString());
            lec.setRank(txtRank.getText().trim());
            new LecturerDaoImpl().addLecturer(lec);
        } catch (SQLException ex) {
            Logger.getLogger(add_view_lecturer_profile.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void updateLecturer() {
        try {
            Lecturer lec = new Lecturer();
            lec.setLectuerName(txtName.getText().trim());
            lec.setLevel(comboLevel.getSelectedItem().toString());
            lec.setLecturerId(id);
            lec.setRank(txtRank.getText().trim());
            new LecturerDaoImpl().updateLecturer(lec);
        } catch (SQLException ex) {
            Logger.getLogger(add_view_lecturer_profile.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void deleteLecturer() {
        int selectedRaw = tblLecturers.getSelectedRow();
        if (selectedRaw != -1) {
            try {
                DefaultTableModel dtm = (DefaultTableModel) tblLecturers.getModel();
                new LecturerDaoImpl().deleteLecturer(Validations.getIntOrZeroFromString(dtm.getValueAt(selectedRaw, 0).toString()));
            } catch (SQLException ex) {
                Logger.getLogger(add_view_lecturer_profile.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    private void loadDataToTable() {
        try {
            String columnList[] = {"lecturer_id", "lecturer_name", "lecturer_level", "Rank"};
            ResultSet rset = new LecturerDaoImpl().getAllLecturers();
            CommonController.loadDataToTable(tblLecturers, rset, columnList);
        } catch (SQLException ex) {
            Logger.getLogger(add_view_lecturer_profile.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

